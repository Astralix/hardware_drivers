function data = DwPhyTest_RunSuite(Suite, PartID, AutoSave, DualBand, opt)
%data = DwPhyTest_RunSuite(Suite, PartID, AutoSave, DualBand, opt)
%
%   Suite           Description
%   --------------  -----------------------------------------------------------------
%   BasicRX         (  7 min) Device/DwPhy go/no-go test
%   BasicRX24       (  6 min) Device/DwPhy go/no-go test, 2.4 GHz band
%   BasicRX22       (    min) Device/DwPhy go/no-go test, RF22
%   RxEval          (  4 hrs) Receiver evaluation suite
%   RxEval24        (  3 hrs) Receiver evaluation suite, 2.4 GHz band
%   RxEval22Set1    (1.5 hrs) Receiver evaluation suite 1 (multi-channels), RF22
%   RxEval22Set2    (1.5 hrs) Receiver evaluation suite 2 (single-channel), RF22
%   RxEval22        (  3 hrs) RxEval22Set1 and RxEval22Set2
%   RxSensitivity
%   RxSensitivity24
%   TxEval          ( 32 min) Transmit evaluation suite
%   TxEval24        ( 17 min) Transmit evaluation suite, 2.4 GHz band
%   TxEval22        (    min) Transmit evaluation suite, RF22
%   FullTxEval      (    min) Transmit evaluation suite
%   FullTxEval24    ( 53 min) Transmit evaluation suite, 2.4 GHz band
%   FullTxEval22    ( 28 min) Transmit evaluation suite, RF22
%   FullTxEval96-2  ( 72 min) Transmit evaluation suite, DMW96 + RF52, 2.4 GHz band
%   RxBlocking      (  ? hrs) Receiver blocking tests
%   FullRxEval      (  7 hrs) All RxEval and 54 Mbps Blocking tests
%   FullRxEval24    (  4 hrs) All RxEval and 54 Mbps Blocking tests, 2.4 GHz Band
%   FullRxEval22    (4.3 hrs) All RxEval22 and 54 Mbps Blocking tests, RF22
%   TuneRxGain      (  9 min) Tune RX gain across channels
%   TuneRxGainDPLL  ( 24 min) Tune both RX gain and OFDM DPLL
%   TuneTxPower
%   RxTiming                  Checking Standby-to-RX and TX-to-RX timing
                    
% Written by Barrett Brickner
% Copyright 2008-2011 DSP Group, Inc., All Rights Reserved.

%#ok<*CCAT> -- suppress unwanted warning

%% Default and User Parameters
if nargin<1, Suite   = []; end
if nargin<2, PartID   = []; end
if nargin<3, AutoSave = -1; end
if nargin<4, DualBand = DwPhyLab_Parameters('DualBand'); end
if nargin<5, opt      = []; end

if  isempty(Suite), Suite = 'BasicRX'; end
if ~isempty(DualBand), DwPhyLab_Parameters('DualBand',DualBand); end

clear DwPhyMex; % clear any stale RemoteVWAL connections

data.Description = mfilename;
data.Timestamp = datestr(now);
data.PartID = PartID;
data.Suite = Suite;
data.Result = 1;
data.DwPhyLab_Parameters = DwPhyLab_Parameters;


%% Execute Test Suite
DwPhyTest_Setup; % instrument setup
DwPhyLab_Setup;  % DwPhyLab setup

Suite = GetSuitePseudonym(Suite);
[data.TestList, data.SuiteName] = GetTestList(Suite);

for i=1:length(data.TestList),
    data = RunTest(data, data.TestList{i}, i, opt);
    if AutoSave > 1, % save interim data
        if isempty(PartID),
            filename = sprintf('XXX-%s-XXXXXX-XXXXXX.mat',data.SuiteName);
        else
            filename = sprintf('%s-%s-XXXXXX-XXXXXX.mat',PartID,data.SuiteName);
        end
        save(filename, 'data');
    end
end

data.Runtime = 24*3600*(now-datenum(data.Timestamp));

%% Save/Output Data
if isempty(AutoSave),
    AutoSave = (data.Runtime > 500);
end
if AutoSave,
    t = datenum(now); %#ok<DNNW>
    timestamp = sprintf('%s-%s',datestr(t,'yymmdd'),datestr(t,'HHMMSS'));
    if isempty(PartID),
        filename = sprintf('XXX-%s-%s.mat',data.SuiteName,timestamp);
    else
        filename = sprintf('%s-%s-%s.mat',PartID,data.SuiteName,timestamp);
    end
    save(filename, 'data');
end
if nargout<1, clear data; end; 

%% RunTest
function data = RunTest(data, Name, n, opt)
if data.Result < 0, return ; end; % test suite aborted

if isnumeric(Name),
    Name = sprintf('%g',Name);
end

fprintf('   Running test %s',Name); 
for i=8:-1:length(Name), fprintf(' '); end
fprintf('... ');

% execute the test
try
    TestData = DwPhyTest_RunTest(Name, opt);
catch ME
    fprintf('\n *** TEST ERROR...ABORTING ***\n');
    data.Result = -1;
    data.TestResult(i) = -1;
    data.lasterror = ME;
    return;
end

% display the summary outcome
switch TestData.Result,
    case  0, data.TestResult(n) = 0; fprintf('Fail <---\n'); data.Result = 0;
    case  1, data.TestResult(n) = 1; fprintf('Pass\n');
    case -1, data.TestResult(n) =-1; fprintf('ABORTED *****\n'); data.Result = -1;
    case -2, data.TestResult(n) =-2; fprintf('Error Running Test\n'); data.Result = -2;
    case -9, data.TestResult(n) =-9; fprintf('Unknown Test\n'); data.Result = -9;
    otherwise, error('Unrecognized result code %g',data.TestResult(n));
end

% store the results
TestName = sprintf('Test%s',Name);
TestName(TestName == '.') = '_'; % replace "." with "_"
data.(TestName) = TestData;

function Suite = GetSuitePseudonym(Suite)
%% Replace generic suite name with device/band-specific name

PlatformID = DwPhyLab_Parameters('PlatformID');
DualBand   = DwPhyLab_Parameters('DualBand');
DualRX     = DwPhyLab_Parameters('DualRX');

switch upper(Suite)
    case {'BASICRX','RXEVAL','FULLRXEVAL','TXEVAL','FULLTXEVAL','RXTIMING',...
          'RXSENSITIVITY','RXBLOCKING'},
        if PlatformID > 0,
            if     DualBand, Suite = sprintf('%s96-52',Suite);
            elseif DualRX,   Suite = sprintf('%s96-2', Suite);
            else             Suite = sprintf('%s96',   Suite);
            end
        else
            if     DualBand, Suite = sprintf('%s52',Suite);
            elseif DualRX,   Suite = sprintf('%s24',Suite);
            else             Suite = sprintf('%s22',Suite);
            end
        end
end


